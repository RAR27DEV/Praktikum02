    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        try {
    java.sql.Connection conn=(java.sql.Connection)Aplikasi.konek.koneksiDB();
    String sql = "Select * from tbl_mahasiswa where NoBp='"+cari.getText()+"' or Nama='"+cari.getText()+"'";
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    ResultSet rs = pst.executeQuery(sql);
    if (rs.next()){
        no.setText(rs.getString(1));
        nama.setText(rs.getString(2));
        asal.setText(rs.getString(3));
        tempat.setText(rs.getString(4));
        tgl.setDate(rs.getDate(5));
        alamat.setText(rs.getString(6));
        nohp.setText(rs.getString(7));
        JOptionPane.showMessageDialog(null,"Data tbl_mahasiswa : "
        + cari.getText() + " ditemukan, Apakah Anda Ingin Mengedit atau Menghapus?");
    }
} catch (Exception e){}
cari.requestFocus();
    }//GEN-LAST:event_btn_cariActionPerformed

