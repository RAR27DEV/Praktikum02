    public void tampil_data(){
    DefaultTableModel tabel=new DefaultTableModel();
    tabel.addColumn("NoBP");
    tabel.addColumn("NAMA");
    tabel.addColumn("ASAL SEKOLAH");
    tabel.addColumn("TEMPAT");
    tabel.addColumn("TGL LAHIR");
    tabel.addColumn("ALAMAT");
    tabel.addColumn("NOHP");
    try {
        java.sql.Connection conn = (java.sql.Connection) Aplikasi. konek.koneksiDB();
        String sql = "select * from tbl_mahasiswa";
        java.sql.PreparedStatement pst= conn.prepareStatement ( sql);
        ResultSet rs = pst.executeQuery( sql);
      while(rs.next())
        {
        tabel.addRow(new Object[]{
            rs.getString(1),
                rs.getString(2),
                    rs.getString(3),
                        rs.getString(4),
                            rs.getString(5),
                                rs.getString(6),
                                    rs.getString(7)});
        }
        jTable1.setModel(tabel);
        }
    catch (Exception e){
    }
}
